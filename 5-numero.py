# Los NÚMEROS pueden ser INTEGER( int, enteros) o FLOAT (flotantes, decimales)
# Se pueden hacer todas las operaciones matemáticas

# SUMA: +
suma = 1 + 3
print(suma)  # 4

# RESTA: -
resta = 10 - 2
print(resta)  # 8
print (20  - 3) # 17

# DIVISIÓN: /
print ( 4 / 3 ) # 1.33333
print ( 4 // 3)   # 1 muestra el número sin decimales

# MULTIPLICACIÓN: *
print ( 1 * 1.00) 

# PONTENCIA: **
print ( 2**3)    #8

# MODULO: %
print ( 5 % 2 )  # 1

print( (20-10) / (5* 3**2) )  # 0.222222 con los () separo términos

# Con input() pido que ingreses tu edad, mediante int() lo conviertoa entero y la guardo como valor en la variable edad. Luego creo la variable nueva_edad y le asignoelvalor de: edad + 5. Imprimo la nueva edad
edad = int(input('Inserta tu edad: '))
nueva_edad = edad + 5
